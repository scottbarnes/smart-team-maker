@dataclass
class Team:
    id: int
    members: list

    def fields(self):
        result = []
        for member in self.members:
            result.append(member.field)
        return result

    def nationalities(self):
        result = []
        for member in self.members:
            result.append(member.nationality)
        return result

    def member_count(self):
        return len(self.members)


